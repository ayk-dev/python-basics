n = int(input())

print('*' * 2*n + ' ' * (5 * n - 4 * n) + '*' * 2*n)

for row in range(1, n-1):
    if row == (n - 1) // 2:
        print('*' + ('/' * (n - 1)) * 2 + '*' + (5 * n - 4 * n) * '|' + '*' + ('/' * (n - 1)) * 2 + '*')
    else:
        print('*' + ('/' * (n - 1)) * 2 + '*' + (5 * n - 4 * n) * ' ' + '*' + ('/' * (n - 1)) * 2 + '*')

print('*' * 2*n + ' ' * (5 * n - 4 * n) + '*' * 2*n)
